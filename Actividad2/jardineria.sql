-- ======================================
-- 1. CREACIÓN DE LA BASE DE DATOS
-- ======================================
DROP DATABASE IF EXISTS jardineria;
CREATE DATABASE jardineria;
USE jardineria;

-- ======================================
-- 2. CREACIÓN DE TABLAS
-- ======================================
CREATE TABLE oficina (
  id_oficina VARCHAR(10) PRIMARY KEY,
  ciudad VARCHAR(50),
  pais VARCHAR(50),
  region VARCHAR(50),
  codigo_postal VARCHAR(15),
  telefono VARCHAR(20),
  linea_direccion1 VARCHAR(50),
  linea_direccion2 VARCHAR(50)
);

CREATE TABLE empleado (
  id_empleado INT PRIMARY KEY,
  nombre VARCHAR(50),
  apellido1 VARCHAR(50),
  apellido2 VARCHAR(50),
  extension VARCHAR(10),
  email VARCHAR(100),
  id_oficina VARCHAR(10),
  id_jefe INT,
  puesto VARCHAR(50),
  FOREIGN KEY (id_oficina) REFERENCES oficina(id_oficina),
  FOREIGN KEY (id_jefe) REFERENCES empleado(id_empleado)
);

CREATE TABLE cliente (
  id_cliente INT PRIMARY KEY,
  nombre_cliente VARCHAR(100),
  nombre_contacto VARCHAR(50),
  apellido_contacto VARCHAR(50),
  telefono VARCHAR(20),
  fax VARCHAR(20),
  linea_direccion1 VARCHAR(50),
  linea_direccion2 VARCHAR(50),
  ciudad VARCHAR(50),
  region VARCHAR(50),
  pais VARCHAR(50),
  codigo_postal VARCHAR(15),
  id_empleado_rep_ventas INT,
  limite_credito DECIMAL(15,2),
  FOREIGN KEY (id_empleado_rep_ventas) REFERENCES empleado(id_empleado)
);

CREATE TABLE producto (
  id_producto INT PRIMARY KEY,
  nombre VARCHAR(100) NOT NULL,
  gama VARCHAR(50),
  dimensiones VARCHAR(50),
  proveedor VARCHAR(100),
  descripcion TEXT,
  cantidad_en_stock INT,
  precio_venta DECIMAL(15,2),
  precio_proveedor DECIMAL(15,2)
);

CREATE TABLE pedido (
  id_pedido INT PRIMARY KEY,
  id_cliente INT,
  fecha_pedido DATE,
  fecha_esperada DATE,
  fecha_entrega DATE,
  estado ENUM('Pendiente','Entregado','Rechazado'),
  comentarios TEXT,
  FOREIGN KEY (id_cliente) REFERENCES cliente(id_cliente)
);

CREATE TABLE detalle_pedido (
  id_pedido INT,
  id_producto INT,
  cantidad INT,
  precio_unidad DECIMAL(15,2),
  numero_linea INT,
  PRIMARY KEY (id_pedido, id_producto),
  FOREIGN KEY (id_pedido) REFERENCES pedido(id_pedido),
  FOREIGN KEY (id_producto) REFERENCES producto(id_producto)
);

CREATE TABLE pago (
  id_pago INT AUTO_INCREMENT PRIMARY KEY,
  id_cliente INT,
  forma_pago ENUM('Transferencia','Cheque','Tarjeta'),
  id_transaccion VARCHAR(50),
  fecha_pago DATE,
  total DECIMAL(15,2),
  FOREIGN KEY (id_cliente) REFERENCES cliente(id_cliente)
);

